    void add()
    {
        long phn;
        int age,ex,sl;
        int a;
        String dnam,adres,dojn,qry,id;
        id=did.getText();
        dnam=dname.getText();
        adres=adrs.getText();
        dojn=doj.getText();
        phn=Long.parseLong(pno.getText());
        a=pno.getText().length();
        age=Integer.parseInt(ag.getText());
        ex=Integer.parseInt(exp.getText());
        sl=Integer.parseInt(sal.getText());
        try
        {
            Connection cn;
            Statement st;
            int rs;
            cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/cab","root","GAUsing@2011");
            st=cn.createStatement();
            qry="insert into driver(did,dname,address,doj,phno,age,experience,salary) values('"+id+"','"+dnam+"','"+adres+"'"
            + ",'"+dojn+"',"+phn+","+age+","+ex+","+sl+")";
            rs=st.executeUpdate(qry);
            if(rs>0)
            {
                JOptionPane.showMessageDialog(null,"Driver added successfully");
            }
            cn.close();
        }catch(Exception er)
        {
            JOptionPane.showMessageDialog(null, "Error"+er);
        }
    }
