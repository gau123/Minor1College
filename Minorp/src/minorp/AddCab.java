class validatecab
{
    private void validatecid()
    {
        String a;
        int n;
        n=Integer.parseInt(cid.getText());
        a = cid.getText();
        String pattern = "(\\d{3})";
        Pattern r = Pattern.compile(pattern);
        Matcher m = r.matcher(a);
        if (m.matches( )) 
        {
            if(!((100<n) && (n<200)))
            {
                lbcid.setText("Invalid");
                ab++;
            }
        } 
        else 
        {
            lbcid.setText("Invalid");
            ab++;
        }
    }
    private void validatename()
    {
        String a;
        int n;
        n=cname.getText().length();
        a = cname.getText();
        String pattern = "[a-zA-Z]*";
        Pattern r = Pattern.compile(pattern);
        Matcher m = r.matcher(a);
        if (m.matches( )) 
        {
            if(!((2<n) && (n<40)))
            {
                lbcnam.setText("Invalid");
                ab++;
            }
        } 
        else 
        {
            lbcnam.setText("Invalid");
            ab++;
        }
    }
    
    void validatetyp()
    {
        String ct;
        ct=ctype.getText();
        if(!(ct.equals("Ac") || ct.equals("Non Ac")))
            {
                lbctyp.setText("Invalid Type");
                ab++;
            }
    }
    private void validatecpc()
    {
        String a;
        int n;
        n=Integer.parseInt(cap.getText());
        a = cap.getText();
        String pattern = "(\\d{1})";
        Pattern r = Pattern.compile(pattern);
        Matcher m = r.matcher(a);
        if (m.matches( )) 
        {
            if(!((3<n) && (n<10)))
            {
                lbcpc.setText("Invalid Capacity");
                ab++;
            }
        } 
        else 
        {
            lbcpc.setText("Invalid Capacity");
            ab++;
        }
    }
}
class addcb
{
    void add()
    {
        int cd,cpc;
        String cnm,ctp,qry;
        cd=Integer.parseInt(cid.getText());
        cnm=cname.getText();
        ctp=ctype.getText();
        cpc=Integer.parseInt(cap.getText());
        try
        {
            Connection cn;
            Statement st;
            int rs;
            cn=DriverManager.getConnection("jdbc:mysql://localhost:3306/cab","root","GAUsing@2011");
            st=cn.createStatement();
            qry="insert into cab(cid,cname,ctype,capacity) values("+cd+",'"+cnm+"','"+ctp+"',"+cpc+")";
            rs=st.executeUpdate(qry);
            if(rs>0)
            {
                JOptionPane.showMessageDialog(null,"Cab added successfully");
            }
            cn.close();
        }catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Error"+ex);
        }
    }
    void clear()
    {
        cid.setText("");
        cname.setText("");
        ctype.setText("");
        cap.setText("");
    }
}
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        addcb ob=new addcb();
                
        int dialogbt=JOptionPane.YES_NO_OPTION;
                dialogbt=JOptionPane.showConfirmDialog(null,"Do you want to confirm ?","Confirmation",dialogbt);
                if(dialogbt==JOptionPane.YES_NO_OPTION)
                {
                    if(ab!=0)
                        {
                            JOptionPane.showMessageDialog(null,"One or more fields are invalid");
                        }
                        else
                        {
                            ob.add();
                            ob.clear();
                        }
                    this.setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"New Cab Not Added");
                    this.setVisible(true);
                    ob.clear();
                }       
    }//GEN-LAST:event_jButton1ActionPerformed

